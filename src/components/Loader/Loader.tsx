type LoaderSize = 'x-small' | 'small' | 'medium' | 'large';

type LoaderSizesRecord = {
  size: number;
};

type LoaderCompRecord = {
  comp: React.ReactNode;
};

const LoaderSizes: {
  [key in LoaderSize]: LoaderSizesRecord;
} = {
  'x-small': { size: 14 },
  small: { size: 16 },
  medium: { size: 22 },
  large: { size: 32 }
};

const LoaderComp: {
  [key in LoaderSize]: LoaderCompRecord;
} = {
  'x-small': { comp: LoaderXSmall() },
  small: { comp: LoaderSmall() },
  medium: { comp: LoaderMedium() },
  large: { comp: LoaderLarge() }
};

interface LoaderProps {
  loading?: boolean;
  size?: LoaderSize;
  className?: string;
  color?: string;
  centered?: boolean;
}

export const Loader = React.memo((props: LoaderProps) => {
  const { size = 'medium', className, color = 'var(--accentBlue)' } = props;
  const sizes = LoaderSizes[size];
  const comp = LoaderComp[size];
  const boxSize = sizes.size;
  const loaderElement = comp.comp;
  return (
    <div
      className={classNames('loader',  className)}
      style={
        {
          '--loader-size': boxSize,
          '--loader-color': color
        } as React.CSSProperties
      }
    >
      {loaderElement}
    </div>
  );
});
